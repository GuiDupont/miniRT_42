#include "../libft.h"

int		c_is_flag(char c)
{
	if (c == '-' || c == '.' || c == '+' || c == ' ' || c == '0'
			|| c == '*' || (c >= '0' && c <= '9'))
		return (1);
	return (0);
}

int		c_is_symbol(char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i'
		|| c == 'u' || c == 'x' || c == 'X' || c == '%')
		return (1);
	return (0);
}
